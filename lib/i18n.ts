export type TranslationKey = keyof (typeof messages)["en"];

export function resolveUiLanguage(value: string | null | undefined): UiLanguage {
  const normalized = String(value ?? "").trim().toLowerCase();
  return UI_LANGUAGES.some((language) => language.value === normalized)
    ? (normalized as UiLanguage)
    : "en";
}

export function languageLabel(language: UiLanguage) {
  return UI_LANGUAGES.find((entry) => entry.value === language)?.label ?? "English";
}

export function getHtmlLang(language: UiLanguage) {
  if (language === "fil") {
    return "fil";
  }
  return language;
}

function interpolate(template: string, vars?: Record<string, string | number>) {
  if (!vars) {
    return template;
  }
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(vars[key] ?? ""));
}

export function translate(language: UiLanguage, key: TranslationKey, vars?: Record<string, string | number>) {
  const template = messages[language][key] ?? messages.en[key] ?? key;
  return interpolate(template, vars);
}
